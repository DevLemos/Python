# Cálculo IMC

nome = 'Kauan Lemos'
altura = 1.85
peso = 88
imc = peso / altura ** 2

# Exemplo 1
print(nome, 'tem', altura, 'de altura,')
print('pesa', peso, 'quilos e seu IMC é')
print(imc)

print('')

# Exemplo 2
print(f'{nome} tem {altura:.2f} de altura,')
print(f'pesa {peso} quilos e seu IMC é')
print(f'{imc:.2f}')
